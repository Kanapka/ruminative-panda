class Robot: 
    def forward(speed: int):
        print(f'Going forward with speed {speed}')

    def backward(speed: int):
        print(f'Going backward with speed {speed}')

    def left(speed: int):
        print(f'Going left with speed {speed}')

    def right(speed: int):
        print(f'Going right with speed {speed}')

    def stop():
        print(f'Stopping')

